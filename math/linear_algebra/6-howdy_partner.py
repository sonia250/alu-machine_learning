#!/usr/bin/env python3
"""Module for concatenating two arrays"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays

    Args:
        arr1: First array (list of ints/floats)
        arr2: Second array (list of ints/floats)

    Returns:
        A new list containing elements from both arrays
    """
    return arr1 + arr2
